let currentLang = 'de';

export function setLanguage(lang) { currentLang = lang; document.documentElement.lang = lang; }
export function getLanguage()     { return currentLang; }
export function t(key, params)    {
  let s = S[currentLang]?.[key] || S['en']?.[key] || key;
  if (params) Object.entries(params).forEach(([k,v]) => { s = s.replaceAll(`{${k}}`, String(v)); });
  return s;
}
